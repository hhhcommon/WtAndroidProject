    /**
     * 创建群组数据交互
     *
     * @param Name
     * @param password
     * @param type
     * @param url
     * @param listener 监听
     */
    public void loadNews(String Name, String password, int type, String url, final OnLoadInterface listener) {
        try {
            RetrofitUtils.getInstance().CreateGroup(Name, password, type, url)
                    .subscribeOn(Schedulers.io())
                    .observeOn(AndroidSchedulers.mainThread())
                    .subscribe(new Action1<Object>() {
                        @Override
                        public void call(Object o) {
                            try {
                                Log.e("创建群组返回数据", new GsonBuilder().serializeNulls().create().toJson(o));
                                //填充UI
                                listener.onSuccess(o);
                            } catch (Exception e) {
                                e.printStackTrace();
                                listener.onFailure("");
                            }
                        }
                    }, new Action1<Throwable>() {
                        @Override
                        public void call(Throwable throwable) {
                            throwable.printStackTrace();
                            listener.onFailure("");
                        }
                    });
        } catch (Exception e) {
            e.printStackTrace();
            listener.onFailure("");
        }
    }

    // API 19 以下获取图片路径的方法
